package org.xmx0632.management.api.v1.bo;

import java.util.Date;

public class UpdateRequest {

	private String customerName;
	private String sex;
	private String mobile;
	private String telephone;
	private String province;
	private String city;
	private String district;
	private String county;
	private String address;
	private String area;
	private String zipCode;
	private String email;
	private Date birthday;
	private String username;

	public UpdateRequest() {
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getZipCode() {
		return zipCode;
	}

	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public Date getBirthday() {
		return birthday;
	}

	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	@Override
	public String toString() {
		return "UpdateRequest [customerName=" + customerName + ", sex=" + sex
				+ ", mobile=" + mobile + ", telephone=" + telephone
				+ ", province=" + province + ", city=" + city + ", district="
				+ district + ", county=" + county + ", address=" + address
				+ ", area=" + area + ", zipCode=" + zipCode + ", email="
				+ email + ", birthday=" + birthday + ", username=" + username
				+ "]";
	}

}
